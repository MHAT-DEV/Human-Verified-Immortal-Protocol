// MULTI-TIER LANGUAGE DETECTION & PERSISTENCE UTILITIES
export function detectLanguage(profileCountry?: string): Language {
  // 1. User Saved Preference
  try {
    const saved = localStorage.getItem('human_arena_language');
    if (saved && (saved in i18nTranslations)) {
      return saved as Language;
    }
    
    // Cookie Check
    const cookieMatches = document.cookie.match(/human_arena_lang=([^;]+)/);
    if (cookieMatches) {
      const decoded = decodeURIComponent(cookieMatches[1]);
      if (decoded in i18nTranslations) {
        return decoded as Language;
      }
    }
  } catch (e) {}

  // 2. Geo-IP and Country-based fallbacks (Profile active country mapping)
  if (profileCountry) {
    const countryMapping: Record<string, Language> = {
      TH: 'th',
      US: 'en',
      GB: 'en',
      CN: 'zh',
      ES: 'es',
      MX: 'es',
      BR: 'pt',
      RU: 'ru',
      JP: 'ja',
      KR: 'ko',
      ID: 'id',
      VN: 'vi'
    };
    if (profileCountry in countryMapping) {
      return countryMapping[profileCountry];
    }
  }

  // 3. Browser Language Detected Signals
  try {
    const browserLang = (navigator.language || (navigator as any).userLanguage || '').toLowerCase();
    const listLangs = navigator.languages || [];
    
    const findMatch = (langStr: string): Language | null => {
      if (langStr.startsWith('th')) return 'th';
      if (langStr.startsWith('zh')) return 'zh';
      if (langStr.startsWith('es')) return 'es';
      if (langStr.startsWith('pt')) return 'pt';
      if (langStr.startsWith('ru')) return 'ru';
      if (langStr.startsWith('ja')) return 'ja';
      if (langStr.startsWith('ko')) return 'ko';
      if (langStr.startsWith('id')) return 'id';
      if (langStr.startsWith('vi')) return 'vi';
      if (langStr.startsWith('en')) return 'en';
      return null;
    };

    const matchPrimary = findMatch(browserLang);
    if (matchPrimary) return matchPrimary;

    for (const l of listLangs) {
      const matchSub = findMatch(l.toLowerCase());
      if (matchSub) return matchSub;
    }
  } catch (e) {}

  // 4. Default Fallback
  return 'th';
}

export function saveLanguagePreference(lang: Language): void {
  try {
    // A. LocalStorage
    localStorage.setItem('human_arena_language', lang);

    // B. Cookie Backup
    const expiry = new Date();
    expiry.setFullYear(expiry.getFullYear() + 5); // 5 Years
    document.cookie = `human_arena_lang=${lang}; expires=${expiry.toUTCString()}; path=/; SameSite=Strict`;

    // C. IndexedDB Backup
    const request = indexedDB.open('HumanityArenaDB', 1);
    request.onsuccess = (event: any) => {
      const db = event.target.result;
      if (db.objectStoreNames.contains('identities')) {
        const transaction = db.transaction(['identities'], 'readwrite');
        const store = transaction.objectStore('identities');
        // Retrieve current payload and update language
        const getReq = store.get('player_identity');
        getReq.onsuccess = (e: any) => {
          if (e.target.result) {
            const data = e.target.result;
            try {
              const parsedProfile = JSON.parse(data.profileJson);
              parsedProfile.activeTheme = parsedProfile.activeTheme || 'Default';
              parsedProfile.preferredLanguage = lang;
              data.profileJson = JSON.stringify(parsedProfile);
              store.put(data);
            } catch (err) {}
          }
        };
      }
    };
  } catch (e) {}
}
